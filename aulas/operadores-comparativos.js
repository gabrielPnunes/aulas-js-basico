var a = 2;
var b = "'3";

// == igual valor
console.log( a ==b );

// === igual valor e igual tipo 
console.log( a === b );

// !=  não é igual em valor
console.log( a != b );

// !==  não é igual em valor não é igual em tipo
console.log( a !== b );

// > maior que 
console.log(  a > b );

// >= maior ou igual 
console.log(  a >= b );

// < menor que 
console.log(  a < b );

// >= menor ou igual 
console.log(  a <= b );
