//  =    x = y    x = y
// +=    x += y   x = x + y
// -=    x -= y   x = x - y
// *=    x *= y   x = x * y
// /=    x /= y   x = x / y
// &=    x &= y   x = x & y

var a = 10;
a += 5;
console.log(a);

var b = 10;
b -= 5;
console.log(b);

var c = 10;
c *= 5;
console.log(c);

var d = 10;
d /= 5;
console.log(d)

var e = 10;
e &= 5;
console.log(e)